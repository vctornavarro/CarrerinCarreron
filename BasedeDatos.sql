DROP DATABASE IF EXISTS bd;
CREATE DATABASE bd;
USE bd;

CREATE TABLE JUGADOR(
  	NOMBRE VARCHAR(50),
  	ID INT NOT NULL,
  	PASS VARCHAR(15) NOT NULL,
  	VICTORIAS INT,
  	CARRERINES INT, /*Carrerines son las carreras jugadas*/
  	CIRCUITOFAV VARCHAR(50), /*El m√°s jugado por ese jugador*/
  	TIEMPOTOTALJUGADO VARCHAR(20),
	PRIMARY KEY(ID)
	)ENGINE= InnoDB;

CREATE TABLE CARRERIN(
	ID INT NOT NULL,
	DURACION VARCHAR(20),
	GANADOR VARCHAR(50),
	CIRCUITO VARCHAR(50),
	VUELTARAPIDA VARCHAR (15),
	PRIMARY KEY(ID)
	)ENGINE = InnoDB; 

CREATE TABLE PARTICIPACION(
	ID INT NOT NULL,   
	IDCARRERIN INT NOT NULL,
	FOREIGN KEY(ID) REFERENCES JUGADOR(ID),
	FOREIGN KEY(IDCARRERIN) REFERENCES CARRERIN(ID)	
	)ENGINE = InnoDB;

